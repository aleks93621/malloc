#include "./includes/libft.h"

void	ft_striteri(char *s, void (*f)(unsigned int, char *))
{
	int i;

	i = -1;
	if (s == '\0' || f == '\0')
		return ;
	while (s[++i] != '\0')
	{
		f(i, s + i);
	}
}
