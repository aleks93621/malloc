#include "./includes/libft.h"

void	ft_bzero(void *s, size_t n)
{
	unsigned char	*c;
	int				i;

	i = 0;
	c = (unsigned char*)s;
	while (0 < n)
	{
		c[i] = '\0';
		n--;
		i++;
	}
}
