#include "../includes/ft_printf.h"

int	ft_isparam(int c)
{
	char ch;

	if (c > 127 || c < 0)
		return (0);
	ch = (char)c;
	if (ch == 104 || ch == 106 || ch == 108 || ch == 122 || ch == 32
			|| ch == 35 || ch == 43 || ch == 45 || ch == 46
			|| (ch > 47 && ch < 58))
		return (1);
	else
		return (0);
}
