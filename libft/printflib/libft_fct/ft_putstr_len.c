#include "../includes/ft_printf.h"

void	ft_putstr_len(char const *s, int e)
{
	int i;

	i = 0;
	if (s[0] == '\0' && !s)
		return ;
	while (i < e)
	{
		ft_putchar(s[i]);
		i++;
	}
}
