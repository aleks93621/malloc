#include "../includes/ft_printf.h"

int	ftc(int c)
{
	char ch;

	if (c > 127 || c < 0)
		return (0);
	ch = (char)c;
	if (ch == 115 || ch == 83 || ch == 112 || ch == 100 || ch == 68
			|| ch == 105 || ch == 111 || ch == 79 || ch == 117
			|| ch == 85 || ch == 120 || ch == 88 || ch == 99 || ch == 67 ||
			ch == 98 || ch == 110)
		return (1);
	else
		return (0);
}
